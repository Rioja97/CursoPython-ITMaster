""" Escribir un programa que permita ingresar dos cadenas de caracteres e indicar si son iguales o distintas. """

while(True):
    
    cad1 = input("Ingrese una oración: ")
    cad2 = input("Ingrese otra oración: ")

    if(cad1 == cad2): print("Las cadenas son iguales")

    else: print("Las cadenas son distintas")